# Operadores matemáticos básicos
num_a = int(input('ingrese el primer número: '))
num_b = float(input('ingrese el segundo número: '))

# Realizamos las operaciones
print(num_a + num_b, 'Resultado de la Suma')
print(num_a - num_b, 'Resultado de la Resta')
print(num_a * num_b, 'Resultado de la Multiplicación')
print(num_a / num_b, 'Resultado de la División decimal (flot)')
print(num_a % num_b, 'Resultado del Módulo, resultado el resto de la división entero')
print(num_a // num_b, 'Resultado de la División Entera')
print(num_a ** num_b, 'Resultado de la Potencia')

